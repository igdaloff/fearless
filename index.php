				<h3>The Story</h3>
				<p>Grace finds her life at a crossroads when Jericho Rhodes, a Navy SEAL, and her ex-boyfriend, wills his cryopreserved sperm to her following his death in Afghanistan. Recently married to Vaughn Collins, Grace is torn between her past and present lives. Confronted with the dilemma of a lifetime, she finds solace in the ocean, enabling her to decide what she wants the most.</p>
			</div>
			<h4>Director's Statement</h4>
			<p>We always carry our history with us. Memories can be our most precious treasures or a burden that is overwhelmingly heavy to carry. Most of our memories are about other human beings. To our main character Grace, the most hidden and protected memories are about the greatest love and the greatest sorrow of her life - an ex-boyfriend and a Navy SEAL Jericho.</p>
			<p>Our story takes place in present day wartime America, where Grace's life is affected by Jericho's unexpected death. She must now make choices concerning love, her career, and her desire for children. Life is full of choices, and we make decisions on a daily basis. Some decisions are small, others life-changing. Many times, big decisions need a lot of courage. I personally believe that since we only have one life, we should always live fully and honestly to ourselves and other people. We must have courage to follow our hearts and do what is right. This the ultimate battle, not some war in a distant country.</p>
			<ul>
				<li>Risto Tuominen</li>
				<li>Director</li>
				<li>Los Angeles</li>
			</ul>
